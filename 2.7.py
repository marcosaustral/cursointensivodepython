# armazene o nome de uma pessoa e inclua alguns carateres em branco no inicio e no final do nome. 
# use cada combição de caracteres "\t" e "\n" pelo menos uma vez
# exiba o nome uma vez, em seguida, exiba o nome usando cada uma das tres funcoes: lstrip() rstrip() strip()

nome = "\t Marcos   \n"

print(nome)

print(nome.lstrip())
print(nome.rstrip())
print(nome.strip())
